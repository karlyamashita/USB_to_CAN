/**
* @brief Function implementing the taskButtonUser thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartTaskButtonUser */
void StartTaskButtonUser(void const * argument)
{
  /* USER CODE BEGIN StartTaskButtonUser */
  /* Infinite loop */
static uint8_t buttonPressedCount, buttonReleasedCount = 0;
static uint8_t buttonLast = 0;

  for(;;)
  {
	  if(CheckButtonUser()) {
		  buttonReleasedCount = 0;
		  if(buttonPressedCount++ >= 50) { // debounce button
			  buttonPressedCount = 0;
			  xSemaphoreGive(myBinarySem01Handle); // this currently isn't doing anything
			  if(buttonLast == 0){
				  buttonLast = CheckButtonUser();
			  }
		  }
	  } else {
		  buttonPressedCount = 0;
		  if(buttonReleasedCount++ >= 50) {
			  buttonReleasedCount = 0;
			  buttonLast = CheckButtonUser();
		  }
	  }
    osDelay(1);
  }
  /* USER CODE END StartTaskButtonUser */
}

